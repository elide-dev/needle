package com.justinblank.strings;

class MatchingVars {
    final int charVar;
    final int counterVar;
    final int stateVar;

    MatchingVars(int charVar, int counterVar, int stateVar) {
        this.charVar = charVar;
        this.counterVar = counterVar;
        this.stateVar = stateVar;
    }
}
